package io.agentscope.core.formatter.openai.dto;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import com.fasterxml.jackson.annotation.JsonProperty;

/**
 * OpenAI choice DTO.
 *
 * <p>This class represents a single choice in an OpenAI API response.
 * For non-streaming responses, use "message" field.
 * For streaming responses, use "delta" field.
 *
 * <p>Example non-streaming:
 * <pre>{@code
 * {
 *   "index": 0,
 *   "message": {
 *     "role": "assistant",
 *     "content": "Hello!"
 *   },
 *   "finish_reason": "stop"
 * }
 * }</pre>
 *
 * <p>Example streaming:
 * <pre>{@code
 * {
 *   "index": 0,
 *   "delta": {
 *     "content": "Hello"
 *   },
 *   "finish_reason": null
 * }
 * }</pre>
 */
@JsonIgnoreProperties(ignoreUnknown = true)
public class OpenAIChoice {

    /** Index of this choice (for multi-choice responses). */
    @JsonProperty("index")
    private Integer index;

    /** The message generated by the model (non-streaming). */
    @JsonProperty("message")
    private OpenAIMessage message;

    /** The delta message (streaming). */
    @JsonProperty("delta")
    private OpenAIMessage delta;

    /**
     * The reason the generation stopped.
     * Values: "stop", "length", "tool_calls", "content_filter", or null (still generating).
     */
    @JsonProperty("finish_reason")
    private String finishReason;

    /** Log probability information. */
    @JsonProperty("logprobs")
    private Object logprobs;

    public OpenAIChoice() {}

    public Integer getIndex() {
        return index;
    }

    public void setIndex(Integer index) {
        this.index = index;
    }

    public OpenAIMessage getMessage() {
        return message;
    }

    public void setMessage(OpenAIMessage message) {
        this.message = message;
    }

    public OpenAIMessage getDelta() {
        return delta;
    }

    public void setDelta(OpenAIMessage delta) {
        this.delta = delta;
    }

    public String getFinishReason() {
        return finishReason;
    }

    public void setFinishReason(String finishReason) {
        this.finishReason = finishReason;
    }

    public Object getLogprobs() {
        return logprobs;
    }

    public void setLogprobs(Object logprobs) {
        this.logprobs = logprobs;
    }

    /**
     * Get the effective message (either message or delta).
     *
     * @return the message for non-streaming, delta for streaming
     */
    public OpenAIMessage getEffectiveMessage() {
        return message != null ? message : delta;
    }
}
