package io.agentscope.core.formatter.dashscope.dto;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import com.fasterxml.jackson.annotation.JsonProperty;

/**
 * DashScope choice DTO.
 *
 * <p>This class represents a single choice in a DashScope API response.
 *
 * <p>Example JSON:
 * <pre>{@code
 * {
 *   "message": {
 *     "role": "assistant",
 *     "content": "Hello!"
 *   },
 *   "finish_reason": "stop"
 * }
 * }</pre>
 */
@JsonIgnoreProperties(ignoreUnknown = true)
public class DashScopeChoice {

    /** The message generated by the model. */
    @JsonProperty("message")
    private DashScopeMessage message;

    /**
     * The reason the generation stopped.
     * Values: "stop", "length", "tool_calls", or null (still generating).
     */
    @JsonProperty("finish_reason")
    private String finishReason;

    /** Index of this choice (for multi-choice responses). */
    @JsonProperty("index")
    private Integer index;

    public DashScopeChoice() {}

    public DashScopeMessage getMessage() {
        return message;
    }

    public void setMessage(DashScopeMessage message) {
        this.message = message;
    }

    public String getFinishReason() {
        return finishReason;
    }

    public void setFinishReason(String finishReason) {
        this.finishReason = finishReason;
    }

    public Integer getIndex() {
        return index;
    }

    public void setIndex(Integer index) {
        this.index = index;
    }
}
