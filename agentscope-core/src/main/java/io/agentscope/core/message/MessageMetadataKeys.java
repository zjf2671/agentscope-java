package io.agentscope.core.message;

/**
 * Constants for well-known message metadata keys.
 *
 * <p>This class defines standard metadata keys used across the framework
 * to ensure consistency and avoid magic strings.
 */
public final class MessageMetadataKeys {

    private MessageMetadataKeys() {
        // Prevent instantiation
    }

    /**
     * Metadata key to bypass history merging in multiagent formatters.
     *
     * <p>Messages with this flag set to {@code true} will be kept as separate messages
     * rather than being merged into conversation history when using multiagent formatters.
     *
     * <p><b>Type:</b> Boolean
     * <p><b>Example:</b>
     * <pre>{@code
     * Map<String, Object> metadata = new HashMap<>();
     * metadata.put(MessageMetadataKeys.BYPASS_MULTIAGENT_HISTORY_MERGE, true);
     * Msg msg = Msg.builder()
     *     .role(MsgRole.USER)
     *     .content(TextBlock.builder().text("Important reminder").build())
     *     .metadata(metadata)
     *     .build();
     * }</pre>
     */
    public static final String BYPASS_MULTIAGENT_HISTORY_MERGE = "_bypass_multiagent_history_merge";

    /**
     * Metadata key to mark structured output reminder messages.
     *
     * <p>This flag is used internally by ReActAgent to identify temporary reminder messages
     * that guide the model to use the generate_response tool for structured output.
     *
     * <p><b>Type:</b> Boolean
     * <p><b>Internal use only</b>
     */
    public static final String STRUCTURED_OUTPUT_REMINDER = "_structured_output_reminder";

    /**
     * Metadata key for chat usage statistics.
     *
     * <p>Contains token usage information (input tokens, output tokens, and time)
     * accumulated during model generation. This allows users to track token consumption
     * for cost estimation and usage monitoring.
     *
     * <p><b>Type:</b> {@link io.agentscope.core.model.ChatUsage}
     * <p><b>Example:</b>
     * <pre>{@code
     * Msg msg = agent.call(userMsg).block();
     * ChatUsage usage = msg.getChatUsage();
     * if (usage != null) {
     *     System.out.println("Input tokens: " + usage.getInputTokens());
     *     System.out.println("Output tokens: " + usage.getOutputTokens());
     *     System.out.println("Total tokens: " + usage.getTotalTokens());
     * }
     * }</pre>
     */
    public static final String CHAT_USAGE = "_chat_usage";

    /**
     * Metadata key for structured output data.
     *
     * <p>Contains the structured response data generated by the generate_response tool.
     * This key is used to isolate structured output from other metadata to avoid conflicts.
     *
     * <p><b>Type:</b> {@code Map<String, Object>} or user-defined POJO
     * <p><b>Example:</b>
     * <pre>{@code
     * Msg msg = agent.call(userMsg, WeatherResponse.class).block();
     * WeatherResponse response = msg.getStructuredData(WeatherResponse.class);
     * }</pre>
     */
    public static final String STRUCTURED_OUTPUT = "_structured_output";
}
