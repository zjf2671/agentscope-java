package io.agentscope.examples.bobatea.business.model;

/**
 * Generic API Response wrapper class
 *
 * @param <T> the type of data in the response
 */
public class ApiResponse<T> {

    private boolean success;
    private String message;
    private T data;
    private Integer count;

    public ApiResponse() {}

    public ApiResponse(boolean success, String message) {
        this.success = success;
        this.message = message;
    }

    public ApiResponse(boolean success, String message, T data) {
        this.success = success;
        this.message = message;
        this.data = data;
    }

    public ApiResponse(boolean success, String message, T data, Integer count) {
        this.success = success;
        this.message = message;
        this.data = data;
        this.count = count;
    }

    // Static factory methods for convenience
    public static <T> ApiResponse<T> success(String message) {
        return new ApiResponse<>(true, message);
    }

    public static <T> ApiResponse<T> success(String message, T data) {
        return new ApiResponse<>(true, message, data);
    }

    public static <T> ApiResponse<T> success(String message, T data, Integer count) {
        return new ApiResponse<>(true, message, data, count);
    }

    public static <T> ApiResponse<T> error(String message) {
        return new ApiResponse<>(false, message);
    }

    // Getters and Setters
    public boolean isSuccess() {
        return success;
    }

    public void setSuccess(boolean success) {
        this.success = success;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }

    public Integer getCount() {
        return count;
    }

    public void setCount(Integer count) {
        this.count = count;
    }

    @Override
    public String toString() {
        return "ApiResponse{"
                + "success="
                + success
                + ", message='"
                + message
                + '\''
                + ", data="
                + data
                + ", count="
                + count
                + '}';
    }
}
